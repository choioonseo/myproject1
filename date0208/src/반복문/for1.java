package 반복문;

public class for1 {

	public static void main(String[] args) {
		//3번 반복
		
		for (int i=0; i<3; i++)
		{
			System.out.println("3번 반복");
		}
		
		//100번 반복
		
		for (int i=0; i<100; i++)
		{
			System.out.println("100번 반복" + i);
		}
		
		for (int i=0; i<10;i++)
		{
			System.out.println("10번 반복 " + (i+1) + "번째 실행중");
		}
	}

}
